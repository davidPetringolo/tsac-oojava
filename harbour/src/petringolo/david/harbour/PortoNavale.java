package petringolo.david.harbour;

public class PortoNavale {
	
	private String _nome;
	private int _nImbarcazioni;
	private double _dimensione;
	
	//GETTERS & SETTERS
	public String get_nome() {
		return _nome;
	}
	public void set_nome(String _nome) {
		this._nome = _nome;
	}
	public int get_nImbarcazioni() {
		return _nImbarcazioni;
	}
	public void set_nImbarcazioni(int _nImbarcazioni) {
		this._nImbarcazioni = _nImbarcazioni;
	}
	public double get_dimensione() {
		return _dimensione;
	}
	public void set_dimensione(double d) {
		this._dimensione = d;
	}
	
	
	

}
